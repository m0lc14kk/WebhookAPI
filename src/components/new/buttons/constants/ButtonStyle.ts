/* eslint-disable no-unused-vars */

enum ButtonStyle {
    /**
     * Blue button.
     */
    PRIMARY = 1,

    /**
     * Gray button.
     */
    SECONDARY,

    /**
     * Green button.
     */
    SUCCESS,

    /**
     * Red button.
     */
    DANGER,

    /**
     * Gray button, with special properties to have an URL.
     */
    LINK,

    /**
     * Buttons for shops.
     */
    PREMIUM,
}

export { ButtonStyle }
